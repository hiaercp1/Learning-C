#include<stdio.h>

int main() {
	/*
	
	+ Addition
	- Subtraction
	* Multiplication
	/ Division
	% Remainder
	++ Increment by one
	-- Decrement by one

	*/

	int x = 50;

	printf("x = %d", x);

	printf("\nx + 10 = %d", x+10);

	printf("\nx - 10 = %d", x-10);

	printf("\nx * 10 = %d", x*10);

	printf("\nx / 10 = %d", x/10);

	printf("\nx % 7 = %d", x%7);

	return 0;
}
